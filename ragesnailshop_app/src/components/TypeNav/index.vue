                                                        :data-categoryId="itemCate3.categoryId">{{
    itemCate3.categoryName
                                                        }}</a>
                                                </em>

                                            </dd>
                                        </dl>
                                    </div>
                                </div>
                            </div>

                        </div>
                    </div>
                </transition>
            </div>
            <nav class="nav">
                <a href="###">服装城</a>
                <a href="###">美妆馆</a>
                <a href="###">尚品汇超市</a>
                <a href="###">全球购</a>
                <a href="###">闪购</a>
                <a href="###">团购</a>
                <a href="###">有趣</a>
                <a href="###">秒杀</a>
            </nav>

        </div>
    </div>
</template>

<script>
import { mapState } from 'vuex';
// 引入方式：是将lodash全部引入
// import _ from 'lodash';
// lodash按需引入
import throttle from 'lodash/throttle'

export default {
    name: "TypeNav",
    data() {
        return {
            // 存储用户鼠标移上哪一个一级分类
            currentIndex: -1,
            show: true,
        }
    },
    // 组件挂载完毕，可以向服务器发请求
    mounted() {
        // t通知Vuex发请求，获取数据，存储于仓库当中
        this.$store.dispatch('categoryList');
        // 当组件挂载完毕，改变show为false
        // 如果不是Home路由组件，将TypeNav进行隐藏
        if (this.$route.path != '/home') {
            this.show = false;
        }

    },
    computed: {
        ...mapState({
            //右侧需要的是一个函数，当使用这个计算属性的时候，右侧函数会立即执行一次
            // 注入一个参数state，其实即为大仓库中的数据
            categoryList: state => state.home.categoryList
        })
    },
    methods: {
        //鼠标进入修改响应式数据currentIndex属性
        /*   changeIndex(index) {
              // indez鼠标移上某一个一级分类上
              this.currentIndex = index;
          }, */
        // 三级联动节流
        changeIndex: throttle(function (index) {
            this.currentIndex = index;
        }, 50),
        // 进行路由跳转的方法
        goSearch(event) {
            // this.$router.push('/search')
            // 最好的解决方法：编程式导航+事件委派
            // 解决第一个问题：把子节点当中a标签，加上自定义属性data-categoryName，其余子节点是没有的
            let element = event.target;
            // 获取当前发出事件的带有data-categoryName的节点
            let { categoryname, category1id, category2id, category3id } = element.dataset;
            if (categoryname) { //a标签
                // 整理路由跳转的参数
                let location = { name: 'search' };
                let query = { categoryName: categoryname }
                // 区别是一级二级还是三级
                if (category1id) {
                    query.category1Id = category1id;
                } else if (category2id) {
                    query.category2Id = category2id;
                } else {
                    query.category3Id = category3id;
                }
                location.query = query;
                // 路由跳转
                this.$router.push(location);
            }
        },
        // 将鼠标移入的时候，让商品分类类别进行展示
        enterShow() {
            this.show = true;
        },
        // 一级分类鼠标移出事件回调
        leaveShow() {
            this.currentIndex = -1;
            // 鼠标移除
            if (this.$route.path != '/home') {
                this.show = false;
            }

        },
    }
}
</script>

